import { sprintf, printf } from "https://deno.land/std/fmt/printf.ts";

export function message(male: string, female: string, percent: number = 0) {
  male = `< :::     ****${sprintf("%21s", male)}${sprintf(
    "%*s",
    21 - male.length,
    ""
  )}****      ::: >`;
  female = `< :::::      ****${sprintf("%17s", female)}${sprintf(
    "%*s",
    18 - female.length,
    ""
  )}****      :::: >`;
  const p = `< ::::::::     ****${sprintf("%15.2f%", percent)} X${sprintf(
    "%8-s",
    ""
  )}****     :::::::: >`;
  return `
  ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
  < :::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: >
  < ::::::::::::            ::::::::::::            :::::::::::: >
  < :::::::::                   ::::                   ::::::::: >
  < :::::::         ********     ::     ********         ::::::: >
  < :::::      ****          ****  ****         ****       ::::: >
  < ::::     ****               ****               ****     :::: >
  < :::     ****                 **                 ****     ::: >
  ${male}
  < :::      ****                ❤️                 ****      ::: >
  < ::::      ****                                ****      :::: >
  ${female}
  < ::::::      ****                            ****      :::::: >
  ${p}
  < ::::::::::::   ****                     ****    :::::::::::: >
  < ::::::::::::::   ****                  ****    ::::::::::::: >
  < :::::::::::::::    ****               ****    :::::::::::::: >
  < :::::::::::::::::    ****           ****    :::::::::::::::: >
  < ::::::::::::::::::::   ****        ****   :::::::::::::::::: >
  < ::::::::::::::::::::::  ***       ***   :::::::::::::::::::: >
  < ::::::::::::::::::::::::  ***   ***   :::::::::::::::::::::: >
  < ::::::::::::::::::::::::::   ***    :::::::::::::::::::::::: >
  < ::::::::::::::::::::::::::::      :::::::::::::::::::::::::: >
  < ::::::::::::::::::::::::::::     ::::::::::::::::::::::::::: >
  < :::::::::::::::::::::::::::::::::::::::::::::::::::::::::::: >
    ()()()()()()()()()()()()()()()()()()()()()()()()()()()()()()
  `;
}
